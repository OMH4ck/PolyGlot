M <- matrix(M, 16, 21)

tryone <- function(flip=TRUE, raz=TRUE) {
  xl <- yl <- c(0,1)
  if(flip) xl <- rev(xl)
  plot(0,0,xlim=xl ,ylim=yl, type="n", xlab="", ylab="",
      main=paste("flip =", flip, ", useRaster =", raz))
  xx <- seq(0,1,length=16)
  yy <- seq(0,1,length=21)
  image(xx, yy, M, add=TRUE, useRaster=raz)
}

tryall <- function() {
  opa <- par(mfrow=c(2,2))
  tryone(FALSE, FALSE)
  tryone(FALSE, TRUE)
  tryone(TRUE, FALSE)
  tryone(TRUE, TRUE)
  par(opa)
  invisible(NULL)
}

